	<h2>Academic Misconduct</h2>
	<hr>
	<p>Academic Misconduct is an act in which a student:</p>
	<ul>
		<li>Seeks to claim credit for the work or efforts of another without authorization or citation.</li>
		<li>Uses unauthorized material or fabricated data in any academic exercise.</li>
		<li>Forges or falsifies academic documents or records.</li>
		<li>Intentionally impedes or damages the academic work of others.</li>
		<li>Engages in conduct aimed at making false representation of a student’s academic performance.</li>
		<li>Assists other students in any of these acts.</li>
	</ul>
	<h3>Collaboration, Cheating, and the Difference Between the Two</h3>
	<p>There are two different kinds of working together: collaborating and plagiarism.</p>
	<p><strong>Collaborating</strong></p>
	<ul>
		<li>Collaborating is good.</li>
		<li>You are encouraged to collaborate on ideas and program design.</li>
		<li>Programming is often a social effort, and there is much you can learn by talking out the ideas in this class with each other.</li>
		<li>If a piece of your program utilizes someone else's idea, i.e., someone other than the program author(s), you must give that person credit.</li>
	</ul>
	<p><strong>Plagiarism</strong></p>
	<ul>
		<li>Plagiarism is bad. DON'T DO IT!</li>
		<li>Any work that you turn in should be your work.</li>
		<li>Programming is often a social effort, and there is much you can learn by talking out the ideas in this class with each other.</li>
		<li>If a piece of your program utilizes someone else's idea, i.e., someone other than the program author(s), you must give that person credit.</li>
	</ul>
	<p>I am compelled by UWEC policy to notify the Department if I find evidence of plagiarism. Note that academic dishonesty includes not only cheating, fabrication, and plagiarism, but also includes helping other students commit acts of academic dishonesty by allowing them to obtain copies of your work. You are allowed to use the Web for reference purposes, but you may not copy material from any website or any other source without proper citations (<a href="https://owl.purdue.edu/owl/research_and_citation/apa_style/apa_formatting_and_style_guide/general_format.html">APA formatting</a>). In short, all submitted work must be your own. Cases of academic dishonesty will be dealt with strictly.</p>
	<p>The academic penalty for a finding of responsibility can range from a grade of zero in the specific assignment to an F in this course.</p>
